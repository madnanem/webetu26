import React, { useState } from 'react';
import { Outlet, NavLink, useNavigate } from 'react-router-dom';
import { useAuth } from '../context/AuthContext';
import { useTheme } from '../context/ThemeContext';
import { useLanguage, useT } from '../context/LanguageContext';
import './Layout.css';

const LANGS = [
  { code: 'en', label: 'EN' },
  { code: 'ar', label: 'ع' },
];

export default function Layout() {
  const { auth, logout } = useAuth();
  const { theme, toggle: toggleTheme } = useTheme();
  const { lang, setLang } = useLanguage();
  const t = useT();
  const navigate = useNavigate();
  const [sidebarOpen, setSidebarOpen] = useState(false);

  const handleLogout = () => { logout(); navigate('/login'); };

  const navItems = [
    { path: '/',             label: t.nav.dashboard,     exact: true, icon: <HomeIcon /> },
    { path: '/profile',      label: t.nav.profile,                    icon: <UserIcon /> },
    { path: '/inscriptions', label: t.nav.inscriptions,               icon: <DocumentIcon /> },
    { path: '/bilans',       label: t.nav.bilans,                     icon: <ChartIcon /> },
    { path: '/groups',       label: t.nav.groups,                     icon: <GroupIcon /> },
    { path: '/conge',        label: t.nav.conge,                      icon: <CongeIcon /> },
//    { path: '/bacnotes',     label: t.nav.bacnotes,                   icon: <BacNotesIcon /> },
    { path: '/timetable',    label: t.nav.timetable,                  icon: <TimetableIcon /> },
    { path: '/examgrades',   label: t.nav.examgrades,                 icon: <ExamIcon /> },
    { path: '/ccgrades',     label: t.nav.ccgrades,                   icon: <CCIcon /> },
  ];

  return (
    <div className="layout">
      <aside className={`sidebar ${sidebarOpen ? 'open' : ''}`}>
        <div className="sidebar-header">
          <div className="logo">
            <img src="/webetu.jpeg" alt="PROGRES" className="logo-img" />
          </div>
          <button className="sidebar-close" onClick={() => setSidebarOpen(false)}>✕</button>
        </div>

        <div className="user-card">
          <div className="user-avatar">{auth?.username?.[0]?.toUpperCase() || 'E'}</div>
          <div className="user-info">
            <span className="user-name">{auth?.username}</span>
            <span className="user-role">{t.nav.student}</span>
          </div>
        </div>

        <nav className="sidebar-nav">
          {navItems.map(item => (
            <NavLink
              key={item.path} to={item.path} end={item.exact}
              className={({ isActive }) => `nav-item ${isActive ? 'active' : ''}`}
              onClick={() => setSidebarOpen(false)}
            >
              <span className="nav-icon">{item.icon}</span>
              <span>{item.label}</span>
            </NavLink>
          ))}
        </nav>

        <div className="sidebar-controls">
          <div className="lang-switcher">
            {LANGS.map(l => (
              <button key={l.code} className={`lang-btn ${lang === l.code ? 'active' : ''}`} onClick={() => setLang(l.code)}>
                {l.label}
              </button>
            ))}
          </div>
          <button className="theme-toggle" onClick={toggleTheme}>
            {theme === 'dark' ? <SunIcon /> : <MoonIcon />}
          </button>
        </div>

        <button className="logout-btn" onClick={handleLogout}>
          <LogoutIcon /><span>{t.nav.logout}</span>
        </button>
      </aside>

      {sidebarOpen && <div className="overlay" onClick={() => setSidebarOpen(false)} />}

      <div className="main-wrapper">
        <header className="topbar">
          <button className="menu-btn" onClick={() => setSidebarOpen(true)}><MenuIcon /></button>
          <div className="topbar-brand">
            <img src="/webetu.jpeg" alt="PROGRES" className="logo-img-sm" />
          </div>
        </header>
        <main className="main-content"><Outlet /></main>
      </div>
    </div>
  );
}

